CREATE OR REPLACE FUNCTION public.delete_savings_goal(p_account_id bigint)
RETURNS void
LANGUAGE plpgsql
SECURITY INVOKER
SET search_path = ''
AS $$
BEGIN
  DELETE FROM public.transactions
  WHERE account_id = p_account_id;

  DELETE FROM public.accounts
  WHERE id = p_account_id
    AND type = 'savings_goal'::public.account_type;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'Savings goal account % not found', p_account_id;
  END IF;
END;
$$;

CREATE OR REPLACE FUNCTION get_savings_goals (
  p_limit INTEGER DEFAULT NULL,
  p_status public.savings_goal_status DEFAULT NULL,
  p_period_from DATE DEFAULT NULL
) RETURNS TABLE (
  id BIGINT,
  name TEXT,
  currency_code TEXT,
  target_date public.savings_goals.target_date%TYPE,
  status public.savings_goal_status,
  target_amount public.savings_goals.target_amount%TYPE,
  accumulated_amount NUMERIC,
  has_transfers BOOLEAN
) LANGUAGE sql
STABLE
SET search_path = '' AS $$
  WITH goal_data AS (
    SELECT
      acc.id,
      acc.name,
      cur.code AS currency_code,
      sg.target_date,
      sg.target_amount,
      sg.is_cancelled,
      sg.is_done,
      COALESCE(SUM(t.charged_amount), 0) AS accumulated_amount,
      COUNT(t.id) > 0 AS has_transfers,
      acc.created_at
    FROM public.accounts acc
    JOIN public.currencies cur ON cur.id = acc.currency_id
    JOIN public.savings_goals sg ON sg.account_id = acc.id
    LEFT JOIN public.transactions t ON t.account_id = acc.id
    WHERE acc.type = 'savings_goal'::public.account_type
      AND (p_period_from IS NULL OR sg.target_date >= p_period_from)
    GROUP BY acc.id, acc.name, cur.code, sg.target_date, sg.target_amount, sg.is_cancelled, sg.is_done, acc.created_at
  ),
  goal_with_status AS (
    SELECT
      id,
      name,
      currency_code,
      target_date,
      target_amount,
      accumulated_amount,
      has_transfers,
      created_at,
      CASE
        WHEN is_cancelled THEN 'cancelled'::public.savings_goal_status
        WHEN is_done      THEN 'done'::public.savings_goal_status
        WHEN accumulated_amount > 0 THEN 'in_progress'::public.savings_goal_status
        ELSE 'not_started'::public.savings_goal_status
      END AS status
    FROM goal_data
  )
  SELECT id, name, currency_code, target_date, status, target_amount, accumulated_amount, has_transfers
  FROM goal_with_status
  WHERE (p_status IS NULL OR status = p_status)
  ORDER BY created_at DESC
  LIMIT p_limit;
$$;

CREATE OR REPLACE FUNCTION public.get_monthly_savings_overview(
  p_year          integer,
  p_currency_code text
)
RETURNS TABLE(
  month                date,
  current_year_amount  numeric,
  previous_year_amount numeric
)
LANGUAGE sql
STABLE
SET search_path TO ''
AS $function$
WITH
  months AS (
    SELECT gs::date AS month
    FROM generate_series(
      make_date(p_year, 1, 1),
      make_date(p_year, 12, 1),
      interval '1 month'
    ) AS gs
  ),
  savings_accounts AS (
    SELECT acc.id AS account_id
    FROM public.accounts acc
    JOIN public.currencies cur ON cur.id = acc.currency_id
    WHERE acc.type = 'savings_goal'::public.account_type
      AND cur.code  = p_currency_code
  ),
  current_year AS (
    SELECT
      date_trunc('month', (t.transacted_at + t.local_offset))::date AS month,
      SUM(t.charged_amount)                                          AS amount
    FROM public.transactions t
    JOIN savings_accounts sa ON sa.account_id = t.account_id
    WHERE (t.transacted_at + t.local_offset) >= make_date(p_year, 1, 1)::timestamp
      AND (t.transacted_at + t.local_offset) <  make_date(p_year + 1, 1, 1)::timestamp
    GROUP BY date_trunc('month', (t.transacted_at + t.local_offset))
  ),
  previous_year AS (
    SELECT
      (date_trunc('month', (t.transacted_at + t.local_offset))
        + interval '1 year')::date                                   AS month,
      SUM(t.charged_amount)                                          AS amount
    FROM public.transactions t
    JOIN savings_accounts sa ON sa.account_id = t.account_id
    WHERE (t.transacted_at + t.local_offset) >= make_date(p_year - 1, 1, 1)::timestamp
      AND (t.transacted_at + t.local_offset) <  make_date(p_year, 1, 1)::timestamp
    GROUP BY date_trunc('month', (t.transacted_at + t.local_offset))
  )
SELECT
  m.month,
  COALESCE(cy.amount, 0) AS current_year_amount,
  COALESCE(py.amount, 0) AS previous_year_amount
FROM months          m
LEFT JOIN current_year  cy ON cy.month = m.month
LEFT JOIN previous_year py ON py.month = m.month
ORDER BY m.month;
$function$;