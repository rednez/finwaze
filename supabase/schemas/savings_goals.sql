create table public.savings_goals (
  id bigint generated by default as identity not null,
  created_at timestamp with time zone not null default now(),
  user_id uuid not null default auth.uid (),
  target_date date not null,
  target_amount numeric not null,
  is_cancelled boolean not null default false,
  is_done boolean NOT NULL DEFAULT false,
  account_id bigint not null,
  constraint savings_goals_pkey primary key (id),
  constraint savings_goals_account_id_key unique (account_id),
  constraint savings_goals_account_id_fkey foreign KEY (account_id) references accounts (id) on delete CASCADE,
  constraint savings_goals_user_id_fkey foreign KEY (user_id) references auth.users (id)
);

ALTER TABLE savings_goals ENABLE ROW LEVEL SECURITY;

create policy "Enable users to view their own data only"
on "public"."savings_goals"
as PERMISSIVE
for SELECT
to authenticated
using (
  (( SELECT auth.uid() AS uid) = user_id)
);

create policy "Enable insert for users based on user_id"
on "public"."savings_goals"
as PERMISSIVE
for INSERT
to public
with check (
  (select auth.uid()) = user_id
);

CREATE POLICY "Enable update their own data only" ON "public"."savings_goals" AS PERMISSIVE
FOR UPDATE
  TO authenticated USING (
    (
      user_id = (
        SELECT
          auth.uid () AS uid
      )
    )
  );

create policy "Enable delete for users based on user_id"
on "public"."savings_goals"
as PERMISSIVE
for DELETE
to public
using (
  (select auth.uid()) = user_id
);


CREATE OR REPLACE FUNCTION public.create_savings_goal(
  p_name text,
  p_currency_id bigint,
  p_target_amount numeric,
  p_target_date date
)
RETURNS bigint
LANGUAGE plpgsql
SECURITY INVOKER
SET search_path = ''
AS $$
DECLARE
  v_account_id bigint;
BEGIN
  INSERT INTO public.accounts (name, currency_id, type)
  VALUES (p_name, p_currency_id, 'savings_goal'::public.account_type)
  RETURNING id INTO v_account_id;

  INSERT INTO public.savings_goals (account_id, target_amount, target_date)
  VALUES (v_account_id, p_target_amount, p_target_date);

  RETURN v_account_id;
END;
$$;

CREATE OR REPLACE FUNCTION public.update_savings_goal(
  p_account_id bigint,
  p_name text,
  p_target_amount numeric,
  p_target_date date
)
RETURNS void
LANGUAGE plpgsql
SECURITY INVOKER
SET search_path = ''
AS $$
BEGIN
  UPDATE public.accounts
  SET name = p_name
  WHERE id = p_account_id
    AND type = 'savings_goal'::public.account_type;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'Savings goal account % not found', p_account_id;
  END IF;

  UPDATE public.savings_goals
  SET target_amount = p_target_amount,
      target_date   = p_target_date
  WHERE account_id = p_account_id;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'Savings goal record for account % not found', p_account_id;
  END IF;
END;
$$;

CREATE OR REPLACE FUNCTION public.cancel_savings_goal(p_account_id bigint)
RETURNS void
LANGUAGE plpgsql
SECURITY INVOKER
